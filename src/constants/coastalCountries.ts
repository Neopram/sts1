// Helper function to get coastal countries by region
export const getCoastalCountriesByRegion = (region: string) => {
  return COASTAL_COUNTRIES.filter(country => country.region === region);
};

// Helper function to check if a location is a valid coastal country
export const isValidCoastalCountry = (location: string): boolean => {
  return COASTAL_COUNTRIES.some(
    country => 
      country.name.toLowerCase() === location.toLowerCase() ||
      country.code.toLowerCase() === location.toLowerCase()
  );
};

